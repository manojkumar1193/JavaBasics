package java_tutorials;

public class Switch {

	public static void main(String[] args) {
		int var= 10;
		switch(var) {
		case 1: System.out.print("1");
		case 2: System.out.print("1");break;
		case 10: System.out.print("1");//break;
		case 5: System.out.print("1");
		default: System.out.print("10");
		}

	}

}
